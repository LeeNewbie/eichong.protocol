package com.epcentre.net.http;

import java.util.HashMap;

public class HttpPostReq {
	 private String serverIp;
	 private int serverPort;
	 public String getServerIp() {
		return serverIp;
	}
	public void setServerIp(String serverIp) {
		this.serverIp = serverIp;
	}
	public int getServerPort() {
		return serverPort;
	}
	public void setServerPort(int serverPort) {
		this.serverPort = serverPort;
	}
	private  String baseUri;
	 private  String method;
	 private  HashMap<String,Object> params;
	 private  int partnerId;
	 private  String partnerClientId;
	 private boolean repeatSend;
	 
	 public boolean isRepeatSend() {
		return repeatSend;
	}
	public void setRepeatSend(boolean repeatSend) {
		this.repeatSend = repeatSend;
	}
	private int sendNum;
	public int getSendNum() {
		return sendNum;
	}
	public void setSendNum(int sendNum) {
		this.sendNum = sendNum;
	}
	
	
	public String getPartnerClientId() {
		return partnerClientId;
	}
	public void setPartnerClientId(String partnerClientId) {
		this.partnerClientId = partnerClientId;
	}
	public String getBaseUri() {
		return baseUri;
	}
	public void setBaseUri(String baseUri) {
		this.baseUri = baseUri;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public HashMap<String, Object> getParams() {
		return params;
	}
	public void setParams(HashMap<String, Object> params) {
		this.params = params;
	}
	public int getPartnerId() {
		return partnerId;
	}
	public void setPartnerId(int partnerId) {
		this.partnerId = partnerId;
	}
	 
}
